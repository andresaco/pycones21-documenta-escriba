def sum(a: int, b: int) -> int:
    """
    Let's sum two integers!

    :param a: An integer
    :param b: Another integer

    :return: the sum of both parameters
    """
    return a + b

def div(a: int, b: int) -> int:
    """
    Division, mind the zero!
    
    :param a: An integer
    :param b: Another integer

    :return: the result of dividing a and b
    """
    return a / b